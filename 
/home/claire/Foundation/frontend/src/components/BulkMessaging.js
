
import React, { useState, useEffect } from 'react';
import API_BASE from '../utils/apiConfig';

function BulkMessaging({ userRole, userId }) {
  const [categories, setCategories] = useState([]);
  const [selectedCategory, setSelectedCategory] = useState('');
  const [message, setMessage] = useState('');
  const [loading, setLoading] = useState(true);
  const [error, setError] = useState('');

  useEffect(() => {
    const headers = {
      'Content-Type': 'application/json',
      'User-Role': userRole || 'admin',
      'User-ID': userId || ''
    };
    
    fetch(`${API_BASE}/members/categories`, { headers })
      .then(res => {
        if (!res.ok) throw new Error('Failed to fetch categories');
        return res.json();
      })
      .then(data => {
        if (Array.isArray(data)) {
          setCategories(data);
        }
        setLoading(false);
      })
      .catch(err => {
        console.log('Error fetching categories', err);
        setError('Could not load categories. Please try again.');
        setLoading(false);
      });
  }, [userRole, userId]);

  const handleSend = (e) => {
    e.preventDefault();
    if (!message.trim()) {
      alert('Please enter a message');
      return;
    }
    
    const confirmSend = window.confirm(
      `Send SMS to ${selectedCategory || 'ALL'} members?\n\nMessage: "${message}"\n\nThis will automatically send SMS to all registered phone numbers.`
    );
    
    if (confirmSend) {
      const headers = {
        'Content-Type': 'application/json',
        'User-Role': userRole || 'admin',
        'User-ID': userId || ''
      };
      
      fetch(`${API_BASE}/send-bulk-sms`, {
        method: 'POST',
        headers: headers,
        body: JSON.stringify({
          category: selectedCategory,
          message: message
        })
      })
        .then(res => res.json())
        .then(data => {
          alert(`SMS sent successfully to ${data.recipients || 0} members!\n\nDelivery Status: ${data.status}\nSent at: ${new Date().toLocaleString()}`);
          setMessage('');
        })
        .catch(err => alert('Error sending SMS: ' + err.message));
    }
  };

  if (loading) {
    return (
      <div className="form-container" style={{textAlign: 'center', padding: '50px'}}>
        <p>Loading...</p>
      </div>
    );
  }

  return (
    <div className="form-container">
      <h2 className="page-title">SMS Broadcast System</h2>
      <p style={{textAlign: 'center', color: '#666', marginBottom: '30px'}}>Send instant SMS updates to all registered members</p>
      
      {error && (
        <div style={{textAlign: 'center', padding: '10px', color: 'red', marginBottom: '20px'}}>
          <p>{error}</p>
        </div>
      )}
      
      <form onSubmit={handleSend}>
        <div style={{marginBottom: '20px'}}>
          <label style={{display: 'block', marginBottom: '8px', fontWeight: 'bold', color: '#1a237e'}}>Target Audience:</label>
          <select value={selectedCategory} onChange={e => setSelectedCategory(e.target.value)} style={{width: '100%'}}>
            <option value="">All Members (Broadcast to Everyone)</option>
            {categories.map(cat => <option key={cat} value={cat}>{cat} Members Only</option>)}
          </select>
        </div>
        
        <div style={{marginBottom: '20px'}}>
          <label style={{display: 'block', marginBottom: '8px', fontWeight: 'bold', color: '#1a237e'}}>Message Content:</label>
          <textarea 
            placeholder="Type your message here... (e.g., Meeting tomorrow at 2PM, venue: Community Hall)" 
            value={message} 
            onChange={e => setMessage(e.target.value)}
            required
            style={{width: '100%', height: '120px', resize: 'vertical'}}
          />
          <small style={{color: '#666'}}>Characters: {message.length}/160</small>
        </div>
        <button type="submit" style={{
          background: 'linear-gradient(45deg, #1a237e, #283593)',
          padding: '15px 30px',
          fontSize: '16px',
          fontWeight: 'bold',
          color: 'white',
          border: 'none',
          borderRadius: '8px',
          cursor: 'pointer'
        }}>Send SMS to All Members</button>
      </form>
    </div>
  );
}

export default BulkMessaging;

